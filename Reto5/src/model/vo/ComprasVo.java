package model.vo;

public class ComprasVo {

    private int idcompra;
    private String constructoraC;
    private String banco;
    

    public String getBanco() {
        return banco;
    }
    public void setBanco(String banco) {
        this.banco = banco;
    }
    public String getConstructoraC() {
        return constructoraC;
    }
    public void setConstructoraC(String constructoraC) {
        this.constructoraC = constructoraC;
    }
    public int getIdcompra() {
        return idcompra;
    }
    public void setIdcompra(int idcompra) {
        this.idcompra = idcompra;
    }
    
}
